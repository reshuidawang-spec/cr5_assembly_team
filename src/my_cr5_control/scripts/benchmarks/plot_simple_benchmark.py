#!/usr/bin/env python3
"""Simple benchmark plotter — generates performance comparison plots (success rate, planning time) across planners on the simple task dataset."""

import argparse
import csv
import math
import statistics
from collections import defaultdict
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt


REPO_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_PLOT_DATA = (
    REPO_ROOT / "test_results" / "benchmarks" / "simple" / "aggregates" / "planner_comparison_simple_plot_data_metrics.csv"
)
DEFAULT_PLOT_SUMMARY = (
    REPO_ROOT
    / "test_results"
    / "benchmarks"
    / "simple"
    / "aggregates"
    / "planner_comparison_simple_plot_summary_metrics.csv"
)
DEFAULT_PLOTS_ROOT = REPO_ROOT / "test_results" / "benchmarks" / "simple" / "plots"

PLANNER_ORDER = [
    "RRTConnect",
    "RRTstar",
    "LBTRRT",
    "FMT",
    "BFMT",
    "PRMstar",
    "HeuristicGuided",
]

SCENE_ORDER = [
    "Easy_TopCenter",
    "Medium_SideSurface",
    "MediumPlus_RightUpperAngled",
    "Hard_HoleShallow",
    "HardPlus_HoleEdgeOffset",
    "Extreme_HoleDeep",
]

FAST_THRESHOLD_MS = 1000.0
TIMEOUTISH_THRESHOLD_MS = 10000.0


def parse_args():
    """Parse args."""
    parser = argparse.ArgumentParser(
        description="Generate plots for the simple planner benchmark."
    )
    parser.add_argument(
        "--plot-data",
        default=str(DEFAULT_PLOT_DATA),
        help="Detailed benchmark csv generated by planner_comparison_simple_node.",
    )
    parser.add_argument(
        "--plot-summary",
        default=str(DEFAULT_PLOT_SUMMARY),
        help="Summary benchmark csv generated by planner_comparison_simple_node.",
    )
    parser.add_argument(
        "--benchmark-version",
        default="latest",
        help="Benchmark version to filter. Use 'latest' to select the newest version in the summary csv.",
    )
    parser.add_argument(
        "--timestamp",
        default="latest",
        help="Experiment timestamp to filter. Use 'latest' to select the newest run for the chosen version.",
    )
    parser.add_argument(
        "--output-dir",
        default="",
        help="Optional output directory. Default: test_results/benchmarks/simple/plots/<version>/<timestamp>/",
    )
    return parser.parse_args()


def read_csv_rows(path):
    """Read CSV file and return rows as a list of dicts."""
    with open(path, "r", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def resolve_existing_input(path_str, fallback_name):
    """Resolve existing input."""
    path = Path(path_str)
    if path.exists():
        return path
    fallback = REPO_ROOT / "test_results" / "benchmarks" / "simple" / "legacy" / fallback_name
    if fallback.exists():
        return fallback
    return path


def time_key(rows):
    """Time key."""
    if rows and "墙钟时间(ms)" in rows[0]:
        return "墙钟时间(ms)"
    return "规划时间(ms)"


def percentile(values, q):
    """Percentile."""
    if not values:
        return float("nan")
    sorted_values = sorted(values)
    if len(sorted_values) == 1:
        return sorted_values[0]
    position = (len(sorted_values) - 1) * q
    lower = math.floor(position)
    upper = math.ceil(position)
    if lower == upper:
        return sorted_values[int(position)]
    lower_value = sorted_values[lower]
    upper_value = sorted_values[upper]
    weight = position - lower
    return lower_value + (upper_value - lower_value) * weight


def resolve_benchmark_version(summary_rows, requested_version):
    """Resolve benchmark version."""
    versions = [row["基准版本"] for row in summary_rows if row.get("基准版本")]
    if not versions:
        raise RuntimeError("No benchmark versions found in summary csv.")
    if requested_version != "latest":
        if requested_version not in versions:
            raise RuntimeError(f"Benchmark version not found: {requested_version}")
        return requested_version
    return versions[-1]


def resolve_timestamp(summary_rows, benchmark_version, requested_timestamp):
    """Resolve timestamp."""
    timestamps = [
        row["实验时间戳"]
        for row in summary_rows
        if row.get("基准版本") == benchmark_version and row.get("实验时间戳")
    ]
    if not timestamps:
        raise RuntimeError(f"No timestamps found for benchmark version: {benchmark_version}")
    if requested_timestamp != "latest":
        if requested_timestamp not in timestamps:
            raise RuntimeError(f"Timestamp not found for benchmark version: {requested_timestamp}")
        return requested_timestamp
    return timestamps[-1]


def filter_rows(rows, benchmark_version, timestamp):
    """Filter rows."""
    filtered = [
        row
        for row in rows
        if row.get("基准版本") == benchmark_version and row.get("实验时间戳") == timestamp
    ]
    if not filtered:
        raise RuntimeError(
            f"No rows found for benchmark version '{benchmark_version}' and timestamp '{timestamp}'."
        )
    return filtered


def ordered_unique(values, preferred_order):
    """Ordered unique."""
    present = set(values)
    ordered = [item for item in preferred_order if item in present]
    for item in values:
        if item not in ordered:
            ordered.append(item)
    return ordered


def planner_times(rows, planners):
    """Planner times."""
    t_key = time_key(rows)
    grouped = defaultdict(list)
    for row in rows:
        grouped[row["规划器"]].append(float(row[t_key]))
    return {planner: grouped.get(planner, []) for planner in planners}


def compute_overall_stats(rows, planners):
    """Compute overall stats."""
    t_key = time_key(rows)
    grouped = defaultdict(list)
    for row in rows:
        grouped[row["规划器"]].append(row)

    stats_rows = []
    for planner in planners:
        planner_rows = grouped.get(planner, [])
        times = [float(row[t_key]) for row in planner_rows]
        success_count = sum(row["成功"] == "成功" for row in planner_rows)
        total_count = len(planner_rows)
        stats_rows.append(
            {
                "planner": planner,
                "success_rate_pct": 100.0 * success_count / total_count if total_count else 0.0,
                "success_count": success_count,
                "total_count": total_count,
                "mean_ms": statistics.mean(times) if times else float("nan"),
                "median_ms": statistics.median(times) if times else float("nan"),
                "p90_ms": percentile(times, 0.9),
                "std_ms": statistics.pstdev(times) if len(times) > 1 else 0.0,
                "fast_rate_pct": 100.0 * sum(t < FAST_THRESHOLD_MS for t in times) / total_count
                if total_count
                else 0.0,
                "timeoutish_rate_pct": 100.0 * sum(t >= TIMEOUTISH_THRESHOLD_MS for t in times) / total_count
                if total_count
                else 0.0,
            }
        )
    return stats_rows


def compute_per_scene_stats(rows, planners, scenes):
    """Compute per scene stats."""
    t_key = time_key(rows)
    stats_rows = []
    for scene in scenes:
        scene_rows = [row for row in rows if row["场景名称"] == scene]
        for planner in planners:
            planner_rows = [row for row in scene_rows if row["规划器"] == planner]
            times = [float(row[t_key]) for row in planner_rows]
            success_count = sum(row["成功"] == "成功" for row in planner_rows)
            total_count = len(planner_rows)
            stats_rows.append(
                {
                    "scene": scene,
                    "planner": planner,
                    "success_rate_pct": 100.0 * success_count / total_count if total_count else 0.0,
                    "success_count": success_count,
                    "total_count": total_count,
                    "mean_ms": statistics.mean(times) if times else float("nan"),
                    "median_ms": statistics.median(times) if times else float("nan"),
                    "p90_ms": percentile(times, 0.9),
                    "fast_rate_pct": 100.0 * sum(t < FAST_THRESHOLD_MS for t in times) / total_count
                    if total_count
                    else 0.0,
                    "timeoutish_rate_pct": 100.0 * sum(t >= TIMEOUTISH_THRESHOLD_MS for t in times) / total_count
                    if total_count
                    else 0.0,
                }
            )
    return stats_rows


def write_csv(path, fieldnames, rows):
    """Write a list of dicts to a CSV file with given fieldnames."""
    with open(path, "w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def annotate_bars(ax, bars, value_format="{:.1f}"):
    """Annotate bars."""
    for bar in bars:
        height = bar.get_height()
        ax.annotate(
            value_format.format(height),
            xy=(bar.get_x() + bar.get_width() / 2, height),
            xytext=(0, 3),
            textcoords="offset points",
            ha="center",
            va="bottom",
            fontsize=8,
            rotation=90,
        )


def save_overall_success_rate(overall_stats, output_path):
    """Save overall success rate."""
    planners = [row["planner"] for row in overall_stats]
    values = [row["success_rate_pct"] for row in overall_stats]
    fig, ax = plt.subplots(figsize=(10, 5))
    bars = ax.bar(planners, values, color="#4E79A7")
    annotate_bars(ax, bars)
    ax.set_ylim(0, 110)
    ax.set_ylabel("Success Rate (%)")
    ax.set_title("Overall Success Rate")
    ax.grid(axis="y", alpha=0.25)
    plt.xticks(rotation=20)
    fig.tight_layout()
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def save_overall_time_comparison(overall_stats, output_path):
    """Save overall time comparison."""
    planners = [row["planner"] for row in overall_stats]
    mean_values = [row["mean_ms"] for row in overall_stats]
    median_values = [row["median_ms"] for row in overall_stats]
    x_positions = list(range(len(planners)))
    width = 0.38

    fig, ax = plt.subplots(figsize=(11, 5))
    mean_bars = ax.bar(
        [x - width / 2 for x in x_positions], mean_values, width=width, label="Mean", color="#E15759"
    )
    median_bars = ax.bar(
        [x + width / 2 for x in x_positions], median_values, width=width, label="Median", color="#76B7B2"
    )
    ax.set_xticks(x_positions)
    ax.set_xticklabels(planners, rotation=20)
    ax.set_ylabel("Planning Time (ms)")
    ax.set_title("Overall Planning Time: Mean vs Median")
    ax.set_yscale("log")
    ax.grid(axis="y", alpha=0.25)
    ax.legend()
    annotate_bars(ax, mean_bars)
    annotate_bars(ax, median_bars)
    fig.tight_layout()
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def save_per_scene_median_time(per_scene_stats, planners, scenes, output_path):
    """Save per scene median time."""
    scene_to_stats = defaultdict(dict)
    for row in per_scene_stats:
        scene_to_stats[row["scene"]][row["planner"]] = row

    fig, ax = plt.subplots(figsize=(13, 6))
    width = 0.11
    x_positions = list(range(len(scenes)))

    for planner_index, planner in enumerate(planners):
        offsets = [x + (planner_index - (len(planners) - 1) / 2) * width for x in x_positions]
        values = [scene_to_stats[scene][planner]["median_ms"] for scene in scenes]
        ax.bar(offsets, values, width=width, label=planner)

    ax.set_xticks(x_positions)
    ax.set_xticklabels(scenes, rotation=20, ha="right")
    ax.set_ylabel("Median Planning Time (ms)")
    ax.set_title("Per-Scene Median Planning Time")
    ax.set_yscale("log")
    ax.grid(axis="y", alpha=0.25)
    ax.legend(ncol=4, fontsize=8)
    fig.tight_layout()
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def save_timeout_heatmap(per_scene_stats, planners, scenes, output_path):
    """Save timeout heatmap."""
    scene_to_stats = defaultdict(dict)
    for row in per_scene_stats:
        scene_to_stats[row["scene"]][row["planner"]] = row

    values = [
        [scene_to_stats[scene][planner]["timeoutish_rate_pct"] for planner in planners]
        for scene in scenes
    ]

    fig, ax = plt.subplots(figsize=(10, 5))
    image = ax.imshow(values, cmap="YlOrRd", aspect="auto", vmin=0, vmax=100)
    ax.set_xticks(range(len(planners)))
    ax.set_xticklabels(planners, rotation=20, ha="right")
    ax.set_yticks(range(len(scenes)))
    ax.set_yticklabels(scenes)
    ax.set_title("Timeout-Like Ratio (>= 10s)")

    for scene_index, scene in enumerate(scenes):
        for planner_index, planner in enumerate(planners):
            value = scene_to_stats[scene][planner]["timeoutish_rate_pct"]
            ax.text(planner_index, scene_index, f"{value:.0f}%", ha="center", va="center", fontsize=8)

    fig.colorbar(image, ax=ax, label="Timeout-Like Ratio (%)")
    fig.tight_layout()
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def save_per_scene_boxplots(rows, planners, scenes, output_path):
    """Save per scene boxplots."""
    t_key = time_key(rows)
    fig, axes = plt.subplots(2, 3, figsize=(16, 9), sharey=True)
    axes = axes.flatten()

    for axis, scene in zip(axes, scenes):
        scene_rows = [row for row in rows if row["场景名称"] == scene]
        data = []
        labels = []
        for planner in planners:
            planner_scene_rows = [row for row in scene_rows if row["规划器"] == planner]
            if not planner_scene_rows:
                continue
            data.append([float(row[t_key]) for row in planner_scene_rows])
            labels.append(planner)
        axis.boxplot(data, labels=labels, showfliers=True)
        axis.set_title(scene)
        axis.set_yscale("log")
        axis.tick_params(axis="x", rotation=20)
        axis.grid(axis="y", alpha=0.25)

    for axis in axes[len(scenes) :]:
        axis.axis("off")

    fig.suptitle("Per-Scene Planning Time Distribution")
    fig.tight_layout()
    fig.savefig(output_path, dpi=180)
    plt.close(fig)


def main():
    """Main."""
    args = parse_args()
    plot_data_path = resolve_existing_input(args.plot_data, "planner_comparison_simple_plot_data.csv")
    plot_summary_path = resolve_existing_input(
        args.plot_summary, "planner_comparison_simple_plot_summary.csv"
    )

    summary_rows = read_csv_rows(plot_summary_path)
    benchmark_version = resolve_benchmark_version(summary_rows, args.benchmark_version)
    timestamp = resolve_timestamp(summary_rows, benchmark_version, args.timestamp)

    detail_rows = read_csv_rows(plot_data_path)
    filtered_detail_rows = filter_rows(detail_rows, benchmark_version, timestamp)

    planners = ordered_unique([row["规划器"] for row in filtered_detail_rows], PLANNER_ORDER)
    scenes = ordered_unique([row["场景名称"] for row in filtered_detail_rows], SCENE_ORDER)

    if args.output_dir:
        output_dir = Path(args.output_dir)
    else:
        output_dir = DEFAULT_PLOTS_ROOT / benchmark_version / timestamp
    output_dir.mkdir(parents=True, exist_ok=True)

    overall_stats = compute_overall_stats(filtered_detail_rows, planners)
    per_scene_stats = compute_per_scene_stats(filtered_detail_rows, planners, scenes)

    write_csv(
        output_dir / "overall_stats.csv",
        [
            "planner",
            "success_rate_pct",
            "success_count",
            "total_count",
            "mean_ms",
            "median_ms",
            "p90_ms",
            "std_ms",
            "fast_rate_pct",
            "timeoutish_rate_pct",
        ],
        overall_stats,
    )
    write_csv(
        output_dir / "per_scene_stats.csv",
        [
            "scene",
            "planner",
            "success_rate_pct",
            "success_count",
            "total_count",
            "mean_ms",
            "median_ms",
            "p90_ms",
            "fast_rate_pct",
            "timeoutish_rate_pct",
        ],
        per_scene_stats,
    )

    save_overall_success_rate(overall_stats, output_dir / "overall_success_rate.png")
    save_overall_time_comparison(overall_stats, output_dir / "overall_time_mean_vs_median.png")
    save_per_scene_median_time(per_scene_stats, planners, scenes, output_dir / "per_scene_median_time.png")
    save_timeout_heatmap(per_scene_stats, planners, scenes, output_dir / "per_scene_timeout_heatmap.png")
    save_per_scene_boxplots(filtered_detail_rows, planners, scenes, output_dir / "per_scene_boxplots.png")

    print(f"benchmark_version={benchmark_version}")
    print(f"timestamp={timestamp}")
    print(f"output_dir={output_dir}")


if __name__ == "__main__":
    main()
